#include "ft_ls.h"

int	is_option(char c)
{
	return (c == '1' || c == 'l' || c == 'R' || c == 'a' || c == 'r' ||
	c == 't' || c == 'o' || c == 'n' || c == 'A' || c == 'c' || c == 'i');
}
