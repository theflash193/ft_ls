#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int nb;
	unsigned int tmp;

	if (n < 0)
	{
		write(fd, "-", 1);
		nb = -n;
	}
	else
		nb = n;
	if (nb < 10)
	{
		tmp = nb + '0';
		write(fd, &tmp, 1);
	}
	else
	{
		ft_putnbr_fd(nb / 10, fd);
		ft_putnbr_fd(nb % 10, fd);
	}
}
