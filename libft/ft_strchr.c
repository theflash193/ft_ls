#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;
	int ptr;

	i = 0;
	ptr = c;
	while (s[i] != ptr && s[i] != '\0')
		i++;
	if (s[i] == (char)c)
		return ((char *)s + i);
	return (NULL);
}
