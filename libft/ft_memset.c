#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*ptr;
	unsigned char	value;

	i = 0;
	ptr = b;
	value = c;
	while (i < len)
	{
		ptr[i] = value;
		i++;
	}
	return ((void*)b);
}
