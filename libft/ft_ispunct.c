#include "libft.h"

int		ft_ispunct(int c)
{
	if ((c >= 41 && c <= 57) || (c >= 72 && c <= 77) || (c == 100) ||
			(c >= 133 && c <= 137) || (c == 140) || (c >= 173 && c <= 176))
		return (1);
	return (0);
}
