#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*ptr;

	i = 0;
	ptr = (char *)s;
	while (ptr[i] != '\0')
	{
		i++;
	}
	while (i >= '\0')
	{
		if (ptr[i] == (char)c)
			return ((char *)s + i);
		i--;
	}
	return (NULL);
}
