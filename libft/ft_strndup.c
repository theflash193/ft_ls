#include "libft.h"

char	*ft_strndup(const char *s1, size_t i)
{
	unsigned int	j;
	char			*s2;

	if (!(s2 = (char *)malloc(sizeof(char) * (i + 1))))
		return (NULL);
	j = 0;
	while (j < i)
	{
		s2[j] = s1[j];
		j++;
	}
	s2[j] = '\0';
	return (s2);
}
