#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned char	*ptr;

	ptr = (unsigned char *)malloc(sizeof(ptr) * len);
	if (ptr)
	{
		ft_memcpy(ptr, src, len);
		ft_memcpy(dst, ptr, len);
		free(ptr);
	}
	return ((void *)dst);
}
