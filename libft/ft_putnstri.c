#include "libft.h"

int		ft_putnstri(char *str, int n)
{
	int		i;

	i = 0;
	while (str != '\0' && str[i] != '\0' && i < n)
	{
		write(1, &str[i], 1);
		i++;
	}
	return (i);
}
